#include "main.h"
#include <stdio.h>

/**
* main - that prints the numbers from 1 to 100
* But for multiples of three print -Fizz- instead of the number and for the multiples of five print -Buzz-
*
* Return: 0 sucess
*/

int main(void)
{
	int x;

	for (x = 1; x <= 100; x++)
	{
		if ((x % 3 == 0) && (x % 5 == 0))
		{
			printf("fizzBuzz");
		}
		else if (x % 3 == 0)
		{
			printf("Fizz");
		}
		else if (x % 5 == 0)
		{
			printf("Buzz");
		}
		else
		{
			printf("%d", x);
		}
		if (x != 100)
		{
			printf(" ");
		}
	}
	printf("\n");
	return (0);
}
